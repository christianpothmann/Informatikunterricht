public class Abteilung
{
    private String bezeichnung;
    private int raum;
    
    public void setBezeichnung(String pn)
    {
        bezeichnung = pn;
    }
    
    public String getBezeichnung()
    {
        return bezeichnung;
    }
    
    public void setRaum(int pr)
    {
        raum = pr;
    }
    
    public int getRaum()
    {
        return raum;
    }
}
