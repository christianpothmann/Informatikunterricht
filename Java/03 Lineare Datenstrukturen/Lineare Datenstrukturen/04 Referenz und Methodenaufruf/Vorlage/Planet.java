public class Planet
{
    private String name;
    private double größe;

    public void setName(String pn)
    {
        name = pn;
    }

    public String getName()
    {
        return name;
    }

    public void setGröße(int pg)
    {
        größe = pg;
    }

    public double getGröße()
    {
        return größe;
    }
}